package main

import "fmt"

func main() {
	var(
		a = 10
		b = 2
	)

	fmt.Println("penjumlahan", a, "+", b, ":", a + b)
	fmt.Println("pengurangan", a, "+", b, ":",a - b)
	fmt.Println("perkalian", a, "+", b, ":",a * b)
	fmt.Println("pembagian", a, "+", b, ":",a / b)
	fmt.Println("sisa bagi", a, "+", b, ":",a % b)
}